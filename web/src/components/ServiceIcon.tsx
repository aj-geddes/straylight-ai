/**
 * Renders an SVG icon for known services, or a letter-avatar fallback.
 */
export function ServiceIcon({ name, size = 24, className = '' }: ServiceIconProps) {
  const key = name.toLowerCase();
  const iconFn = SERVICE_ICON_MAP[key];

  if (iconFn) {
    return (
      <span
        className={`inline-flex items-center justify-center ${className}`}
        style={{ width: size, height: size }}
      >
        {iconFn(size)}
      </span>
    );
  }

  // Fallback: letter avatar
  const letter = name.length > 0 ? name.charAt(0).toUpperCase() : '?';
  const hue = name.length > 0
    ? (name.charCodeAt(0) * 37) % 360
    : 200;

  return (
    <span
      aria-hidden="true"
      className={`inline-flex items-center justify-center rounded-lg text-white font-semibold ${className}`}
      style={{
        width: size,
        height: size,
        background: `hsl(${hue}, 60%, 45%)`,
        fontSize: Math.floor(size * 0.45),
      }}
    >
      {letter}
    </span>
  );
}
